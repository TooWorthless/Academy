// Найди HTML-теги с одинаковым открывающим и закрывающим тегами.
{
    const text = "<div>Привет</div><span>Мир</span>";
    console.log(text.match(/<(\w+)>(.*?)<\/\1>/g));
}
// Ожидаемый результат: ["<div>Привет</div>", "<span>Мир</span>"]


// Найди слова, разделённые дефисом, где части одинаковые (например, "тест-тест").
{
    const text = "тест тестер тест-тест ";
    console.log(text.match(/([а-я]+)\-\1/g));
}


// Найди пары парных скобок (круглые, квадратные, фигурные)
{
    const text = "(Привет) [Как] {дела} (всё) [ок]";
    console.log(text.match(/\([а-яё]+\)|\[[а-яё]+\]|\{[а-яё]+\}/ig));
}
// Ожидаемый результат: ["(Привет)", "[Как]", "{дела}", "(всё)", "[ок]"]


// Найди все телефонные номера, но только если код города совпадает
{
    let code = "+5";
    const text = "+5 (495) 123-45-67 и +5 (495) 987-65-43";

    // Экранирование спецсимволов
    code = code.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');

    const regexp = new RegExp(
        `${code}[- ]?\\(?\\d{2,3}\\)?[- ]?[\\d\\s\\-]{8,16}\\d`,
        "g"
    );

    console.log(text.match(regexp));
}
// Ожидаемый результат: ["+5 (495) 123-45-67 и +5 (495) 987-65-43"]